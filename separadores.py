#ARGUMENTO sep DEFINE UM SEPARADOR PARA OS DADOS DA PRINT.
print(1,2,3);
print(1,2,3, sep="-");
print(1,2,3, sep="...");
print(1,2,3, sep="+");
print(1,2,3, sep="!");
print(1,2,3, sep="\n");

#ARGUMENTO end DEFINE O FINAL DE UM STRING.
print(1,2,3, end="\n");
print(1,2,3, end=" fim\n");

